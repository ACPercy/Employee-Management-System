// Employee Class 
export class Employee  {
  constructor(id, eid, firstName, lastName, email, department, position, role, salary) {
    this.id = id;
    this.eid = eid;
    this.firstName = firstName;
    this.lastName = lastName;
    this.email = email;
    this.department = department;
    this.position = position;
    this.role = role;
    this.salary = salary;
  }
}